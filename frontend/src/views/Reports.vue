        generating.value = false
      }
    }
    
    // Êü•ÁúãÊä•Âëä
    const viewReport = (report) => {
      window.open(`/api/reports/${report.id}/view`, '_blank')
    }
    
    // ‰∏ãËΩΩÊä•Âëä
    const downloadReport = (report) => {
